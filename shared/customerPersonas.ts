// Helper functions
export const getPersonasByAgeRange = (minAge: number, maxAge: number): CustomerPersona[] => {
  return CUSTOMER_PERSONAS.filter(p => p.age >= minAge && p.age <= maxAge);
};

export const getPersonasByIndustry = (industry: string): CustomerPersona[] => {
  return CUSTOMER_PERSONAS.filter(p => p.industry.toLowerCase() === industry.toLowerCase());
};

export const getPersonasByNationality = (nationality: string): CustomerPersona[] => {
  return CUSTOMER_PERSONAS.filter(p => p.nationality.toLowerCase().includes(nationality.toLowerCase()));
};

export const getPersonasByTechComfort = (minLevel: number): CustomerPersona[] => {
  return CUSTOMER_PERSONAS.filter(p => p.techComfort >= minLevel);
};

export const getPersonasByIncomeLevel = (level: CustomerPersona['incomeLevel']): CustomerPersona[] => {
  return CUSTOMER_PERSONAS.filter(p => p.incomeLevel === level);
};

export const getRandomPersonas = (count: number): CustomerPersona[] => {
  const shuffled = [...CUSTOMER_PERSONAS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};


