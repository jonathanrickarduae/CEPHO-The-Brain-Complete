// Helper function to get avatar URL with fallback
export function getAvatarUrl(expertId: string, fallbackUrl?: string): string {
  // Normalize the expert ID to match our mapping format
  const normalizedId = expertId.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
  
  const mappedAvatar = avatarMappings[normalizedId];
  if (mappedAvatar) {
    return mappedAvatar;
  }
  
  // Try partial match for names like "Peter Growth" -> "peter-growth"
  const partialMatch = Object.keys(avatarMappings).find(key => 
    normalizedId.includes(key) || key.includes(normalizedId)
  );
  if (partialMatch) {
    return avatarMappings[partialMatch];
  }
  
  return fallbackUrl || `https://api.dicebear.com/7.x/avataaars/svg?seed=${expertId}`;
}

// Get avatar by expert name (more flexible matching)
export function getAvatarByName(name: string): string {
  const normalizedName = name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '');
  
  // Direct match
  if (avatarMappings[normalizedName]) {
    return avatarMappings[normalizedName];
  }
  
  // Try first name + specialty pattern (e.g., "Warren Value")
  const firstName = name.split(' ')[0].toLowerCase();
  const lastName = name.split(' ').slice(1).join('-').toLowerCase();
  
  // Check various patterns
  const patterns = [
    normalizedName,
    `${firstName}-${lastName}`,
    firstName,
    lastName,
  ];
  
  for (const pattern of patterns) {
    const match = Object.keys(avatarMappings).find(key => 
      key === pattern || key.startsWith(pattern) || key.endsWith(pattern)
    );
    if (match) {
      return avatarMappings[match];
    }
  }
  
  return `https://api.dicebear.com/7.x/avataaars/svg?seed=${name}`;
}
